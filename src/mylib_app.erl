-module(mylib_app).

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    io:format("~s\n", [?MODULE]),
    mylib_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

